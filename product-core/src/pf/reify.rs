//! Reify orchestration — plan a verifiable C# projection of the What graph.
//!
//! Contract codegen only (no behaviour transpilation): typed records,
//! Decider frames, the scenario test suite, the §6.3 conformance runner,
//! provenance stamping. `plan_csharp` is pure — it returns the full file
//! set; the CLI adapter writes it. `input_hash` pins the exact inputs
//! (canonical Turtle + decider YAMLs) so `reify check` is a drift gate:
//! generated code that no longer matches the graph hash is stale.

use serde_json::json;

use crate::error::{ProductError, Result};

use super::decider::Decider;
