const README_MD: &str = r##"# {NS} — reified from the What graph

Generated by `product reify csharp` from the `.product` What graph. Files
ending in `.g.cs` / `.g.json` are regenerated on every run — do not edit
them. `<Aggregate>Decider.cs` files are editable stubs, scaffolded once:
author `Decide` + `Evolve` there (§3.3 behaviour is realised, not
transpiled).

## Verify (the same way the spec is verified)

1. `dotnet test` — every Decider scenario runs as an xUnit fact (§6.3).
2. Behavioural conformance against the oracle, per decider:
   `product decider conform <decider-id> --runner "dotnet run --project {NS}.Conformance -- <decider-id>"`
   The recorded verdict feeds `product deliverable done`.
3. Drift gate: `product reify check --out <this directory>` fails when the
   What graph has moved past the graph hash stamped into this code
   (see `Provenance.g.cs` / `provenance.g.json`).
"##;

/// `Runtime.g.cs` for the given namespace.
pub fn runtime_file(header: &str, ns: &str) -> String {
    format!("{header}{}", RUNTIME_CS.replace("{NS}", ns))
}

/// The three project files, namespace-substituted.
pub fn domain_csproj() -> String {
    DOMAIN_CSPROJ.to_string()
}

pub fn conformance_csproj(ns: &str) -> String {
    CONFORMANCE_CSPROJ.replace("{NS}", ns)
}

pub fn tests_csproj(ns: &str) -> String {
    TESTS_CSPROJ.replace("{NS}", ns)
}

/// `README.g.md` for the generated tree.
pub fn readme(ns: &str) -> String {
    README_MD.replace("{NS}", ns)
}

/// `Provenance.g.cs` — assembly metadata binding the built DLL to the exact
/// What it realises (§7.3): product, What version, graph content hash.
pub fn provenance_cs(header: &str, product: &str, what_version: &str, graph_hash: &str) -> String {
    use super::reify_ident::cs_escape;
    format!(
        "{header}using System.Reflection;\n\n[assembly: AssemblyMetadata(\"PF.Product\", \"{}\")]\n[assembly: AssemblyMetadata(\"PF.WhatVersion\", \"{}\")]\n[assembly: AssemblyMetadata(\"PF.GraphHash\", \"sha256:{}\")]\n",
        cs_escape(product),
        cs_escape(what_version),
        graph_hash
    )
}
