//! Static C# templates — runtime substrate, project files, provenance.
//!
//! The runtime (`Runtime.g.cs`) is the small, stable substrate every
//! generated aggregate targets: the command/event interfaces, the
//! `DecisionResult` outcome (events or an invariant-id rejection — §3.3),
//! plus the JSON helpers of the §6.3 conformance wire protocol. Project
//! files pin `net8.0`; the test project pins the xUnit toolchain. All
//! templates are namespace-substituted, otherwise byte-fixed.

const RUNTIME_CS: &str = r##"#nullable enable

using System.Collections.Generic;
using System.Text.Json;

namespace {NS};

/// <summary>An intent from the What event model (§3.2) — handled by exactly one Decider.</summary>
public interface IDomainCommand
{
    /// <summary>The command's id in the What graph.</summary>
    string WireId { get; }
}

/// <summary>A past-tense fact from the What event model (§3.2).</summary>
public interface IDomainEvent
{
    /// <summary>The event's id in the What graph.</summary>
    string WireId { get; }

    /// <summary>The payload fields actually set, keyed by wire name (§6.3 protocol).</summary>
    IReadOnlyDictionary<string, object?> WirePayload();
}

/// <summary>The outcome of a decision (§3.3): emitted events, or a rejection naming the violated invariant.</summary>
public sealed class DecisionResult
{
    private DecisionResult(IReadOnlyList<IDomainEvent> events, string? rejected)
    {
        Events = events;
        RejectedInvariant = rejected;
    }

    public IReadOnlyList<IDomainEvent> Events { get; }

    /// <summary>The id of the invariant that rejected the command, or null when accepted.</summary>
    public string? RejectedInvariant { get; }

    public bool IsRejected => RejectedInvariant is not null;

    public static DecisionResult Emit(params IDomainEvent[] events) => new(events, null);

    public static DecisionResult Reject(string invariantId) => new(System.Array.Empty<IDomainEvent>(), invariantId);
}

/// <summary>JSON helpers for the §6.3 conformance wire protocol.</summary>
public static class PfJson
{
    /// <summary>Split a wire ref — a bare id string, or an object with the id under <paramref name="idKey"/> and an optional "with" payload.</summary>
    public static (string Id, JsonElement? With) Split(JsonElement el, string idKey)
    {
        if (el.ValueKind == JsonValueKind.String) return (el.GetString() ?? "", null);
        var id = el.GetProperty(idKey).GetString() ?? "";
        return el.TryGetProperty("with", out var with) ? (id, with) : (id, null);
    }

    public static long? GetLong(JsonElement? with, string name) =>
        with is { } w && w.TryGetProperty(name, out var v) && v.ValueKind == JsonValueKind.Number ? v.GetInt64() : null;

    public static string? GetString(JsonElement? with, string name) =>
        with is { } w && w.TryGetProperty(name, out var v) && v.ValueKind == JsonValueKind.String ? v.GetString() : null;

    public static bool? GetBool(JsonElement? with, string name) =>
        with is { } w && w.TryGetProperty(name, out var v) && (v.ValueKind == JsonValueKind.True || v.ValueKind == JsonValueKind.False) ? v.GetBoolean() : null;
}
"##;

