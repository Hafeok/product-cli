/// `Runtime.g.cs` for the given namespace.
pub fn runtime_file(header: &str, ns: &str) -> String {
    format!("{header}{}", RUNTIME_CS.replace("{NS}", ns))
}

/// The three project files, namespace-substituted.
pub fn domain_csproj() -> String {
    DOMAIN_CSPROJ.to_string()
}

pub fn conformance_csproj(ns: &str) -> String {
    CONFORMANCE_CSPROJ.replace("{NS}", ns)
}

pub fn tests_csproj(ns: &str) -> String {
    TESTS_CSPROJ.replace("{NS}", ns)
}

/// The oracle-only test csproj — references the Conformance project (which
/// holds the adapter seam) instead of a generated Domain project.
pub fn oracle_tests_csproj(ns: &str) -> String {
    TESTS_CSPROJ
        .replace("{NS}.Domain/{NS}.Domain.csproj", "{NS}.Conformance/{NS}.Conformance.csproj")
        .replace("{NS}", ns)
}

/// `README.g.md` for the generated tree (mode-specific).
pub fn readme(ns: &str, oracle_only: bool) -> String {
    let t = if oracle_only { README_ORACLE_MD } else { README_MD };
    t.replace("{NS}", ns)
}

/// `Provenance.g.cs` — assembly metadata binding the built DLL to the exact
/// What it realises (§7.3): product, What version, graph content hash.
pub fn provenance_cs(header: &str, product: &str, what_version: &str, graph_hash: &str) -> String {
    use super::codegen_ident::cs_escape;
    format!(
        "{header}using System.Reflection;\n\n[assembly: AssemblyMetadata(\"PF.Product\", \"{}\")]\n[assembly: AssemblyMetadata(\"PF.WhatVersion\", \"{}\")]\n[assembly: AssemblyMetadata(\"PF.GraphHash\", \"sha256:{}\")]\n",
        cs_escape(product),
        cs_escape(what_version),
        graph_hash
    )
}
