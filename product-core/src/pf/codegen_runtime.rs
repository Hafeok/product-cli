const README_MD: &str = r##"# {NS} — reified from the What graph

Generated by `product codegen csharp` from the `.product` What graph. Files
ending in `.g.cs` / `.g.yaml` / `.g.json` are regenerated on every run —
do not edit them. `<Aggregate>Decider.cs` files are editable stubs,
scaffolded once: author `Decide` + `Evolve` there (§3.3 behaviour is
realised, not transpiled).

## Verify (the same way the spec is verified)

1. `dotnet test` — every Decider scenario runs as an xUnit fact (§6.3).
2. Behavioural conformance against the oracle, per decider
   (build first so the runner's stdout stays protocol-clean):
   `dotnet build {NS}.Conformance` then
   `product decider conform <decider-id> --runner "dotnet {NS}.Conformance/bin/Debug/net8.0/{NS}.Conformance.dll <decider-id>"`
   The recorded verdict feeds `product deliverable done`.
3. Drift gate: `product codegen check --out <this directory>` fails when the
   What graph has moved past the graph hash stamped into this code
   (see `Provenance.g.cs` / `provenance.g.json`).

When the graph carries flows or UI steps, `<Ns>.Conformance.Tests` also
gains **flow facts** (each §3.2 chain replayed across both seams, with
every step's outcome baked by the oracle at generation time) and
**screen facts** (implement the scaffolded `ScreenAdapter` over your UI;
each surfaced projection must render in every non-waived state and each
offer must be wired to its command — §4.5).

`realise-csharp.cell.g.yaml` is a ready-made §5 task-type wiring these
gates as audits — copy it into your blueprint's `cells/` directory to
dispatch realisation work units over this tree.
"##;

