/// Watch for changes in artifact directories and regenerate AGENTS.md.
pub fn watch_and_regenerate(
    config: &ProductConfig,
    repo_root: &std::path::Path,
) -> Result<(), Box<dyn std::error::Error>> {
    use notify::{Config, Event, RecommendedWatcher, RecursiveMode, Watcher};
    use std::sync::mpsc;
    use std::time::Duration;

    let (tx, rx) = mpsc::channel::<Result<Event, notify::Error>>();
    let mut watcher = RecommendedWatcher::new(
        move |res| { let _ = tx.send(res); },
        Config::default().with_poll_interval(Duration::from_secs(1)),
    )?;

    let dirs = [
        config.resolve_path(repo_root, &config.paths.features),
        config.resolve_path(repo_root, &config.paths.adrs),
        config.resolve_path(repo_root, &config.paths.tests),
        config.resolve_path(repo_root, &config.paths.dependencies),
    ];
    for dir in &dirs {
        if dir.exists() {
            watcher.watch(dir, RecursiveMode::Recursive)?;
        }
    }
    if let Some(cfg) = crate::config::find_config_in_dir(repo_root) {
        watcher.watch(&cfg, RecursiveMode::NonRecursive)?;
    }

    eprintln!("Watching for changes... (press Ctrl+C to stop)");
    regenerate(repo_root)?;

    loop {
        match rx.recv() {
            Ok(Ok(_event)) => {
                while rx.try_recv().is_ok() {}
                std::thread::sleep(Duration::from_millis(200));
                match regenerate(repo_root) {
                    Ok(()) => eprintln!("AGENTS.md regenerated"),
                    Err(e) => eprintln!("Regeneration error: {}", e),
                }
            }
            Ok(Err(e)) => eprintln!("Watch error: {}", e),
            Err(_) => break,
        }
    }
    Ok(())
}

/// Regenerate AGENTS.md from current repo state.
fn regenerate(repo_root: &Path) -> Result<(), Box<dyn std::error::Error>> {
    let cfg_path = crate::config::find_config_in_dir(repo_root).ok_or_else(|| {
        crate::error::ProductError::ConfigError(format!(
            "no product config found under {}",
            repo_root.display()
        ))
    })?;
    let config = ProductConfig::load(&cfg_path)?;
    let features_dir = config.resolve_path(repo_root, &config.paths.features);
    let adrs_dir = config.resolve_path(repo_root, &config.paths.adrs);
    let tests_dir = config.resolve_path(repo_root, &config.paths.tests);
    let deps_dir = config.resolve_path(repo_root, &config.paths.dependencies);

    let loaded =
        crate::parser::load_all_with_deps(&features_dir, &adrs_dir, &tests_dir, Some(&deps_dir))?;
    let graph = KnowledgeGraph::build_with_deps(
        loaded.features, loaded.adrs, loaded.tests, loaded.dependencies,
    );
    let content = generate_agent_md(&config, &graph, repo_root);
    let output_path = repo_root.join(&config.agent_context.output_file);
    crate::fileops::write_file_atomic(&output_path, &content)?;
    Ok(())
}

